class Solution:
    def solveSudoku(self, board: List[List[str]]) -> None:
        """
        Do not return anything, modify board in-place instead.
        """
        # è®¾ç½®row\col\block
        row = [set(range(1, 10)) for _ in range(9)]
        col = [set(range(1, 10)) for _ in range(9)]
        block = [set(range(1, 10)) for _ in range(9)]
        empty = []
        # éå†çŸ©é˜µï¼Œå°†å·²æœ‰çš„æ•°å­—è¿›è¡Œå¤„ç†
        for i in range(len(board)):
            for j in range(len(board[0])):
                if board[i][j] != '.':
                    val = int(board[i][j])
                    row[i].remove(val)
                    col[j].remove(val)
                    block[(i // 3) * 3 + (j // 3)].remove(val)
                else:
                    empty.append((i, j))

        # print(row)
        # print(col)
        # print(block)
        # print(empty)

        # å›æº¯
        def backtrace(n):
            # å½“n==len(empty)æ—¶ï¼Œè¡¨ç¤ºå·²ç»å¤„ç†å®Œäº†ï¼Œè¿”å›True
            if n == len(empty):
                return True
            i, j = empty[n]
            b = (i // 3) * 3 + (j // 3)
            for val in row[i] & col[j] & block[b]:  # é›†åˆçš„äº¤é›†è¿ç®— &æ˜¯ä½äºè¿ç®—ç¬¦
                # print('n:', n, ' : ', row[i] & col[j] & block[b], '  val:', val)
                board[i][j] = str(val)  # å°†i,jåæ ‡æ›´æ–°ä¸ºval
                # ç§»é™¤å·²å ç”¨çš„æ•°å­—
                row[i].remove(val)
                col[j].remove(val)
                block[b].remove(val)
                # é€’å½’
                if backtrace(n + 1):
                    return True
                # æ¢å¤çŠ¶æ€
                row[i].add(val)
                col[j].add(val)
                block[b].add(val)
            return False

        backtrace(0)
