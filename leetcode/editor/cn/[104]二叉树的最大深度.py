class Solution:
    def maxDepth(self, root: TreeNode) -> int:
        ans = 0
        if not root:
            return ans
        queue = []  # å»ºç«‹é˜Ÿåˆ—
        queue.append(root)
        while queue:
            size = len(queue)
            while size > 0:
                cur = queue.pop(0)
                if cur.left is not None:
                    queue.append(cur.left)
                if cur.right is not None:
                    queue.append(cur.right)
                size -= 1  # å¤„ç†å¥½ä¸€ä¸ªèŠ‚ç‚¹
            ans += 1  # å¤„ç†å¥½ä¸€å±‚
        return ans

