# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, val=0, next=None):
#         self.val = val
#         self.next = next
class Solution:
    def reverseList(self, head: ListNode) -> ListNode:
        # 递归终止条件
        if head is None or head.next is None:
            return head
        # 递归调用
        dummy = self.reverseList(head.next)
        # 当前节点处理
        head.next.next = head
        head.next = None
        return dummy
